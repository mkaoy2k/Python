# 算術運算子示範
#
# 這個程式展示了 Python 中基本的算術運算子的使用方式，
# 包括加法、減法、乘法、除法等運算。

# 加法運算
# 使用 + 運算子來進行加法運算
print('3 + 2 等於...')
print('   ', 3 + 2)

# 減法運算
# 使用 - 運算子來進行減法運算
print('3 - 2 等於...')
print('   ', 3 - 2)

# 乘法運算
# 使用 * 運算子來進行乘法運算
print('3 * 2 等於...')
print('   ', 3 * 2)

# 除法運算
# 使用 / 運算子來進行除法運算，結果會是浮點數
print('3 / 2 等於...')
print('   ', 3 / 2)

# 整數除法運算
# 使用 // 運算子來進行整數除法運算，結果會是整數
print('3 // 2 等於...')
print('   ', 3 // 2)

# 指數運算
# 使用 ** 運算子來進行指數運算
print('3 ** 2 等於...')
print('   ', 3 ** 2)

# 取餘數運算
# 使用 % 運算子來進行取餘數運算
print('3 % 2 等於...')
print('   ', 3 % 2)

# 測試偶數或奇數使用取餘數運算
m = 5
if (m % 2):
	print(m, '是奇數!')
else :
	print(m, '是偶數!')

# 內建函數
#
# abs
m = -4
print(m, '的絕對值=', abs(m))

# 四捨五入到最接近的整數
m = 3.45
n = 5.55
print(m, '四捨五入到最接近的整數=', round(m))

# 四捨五入到小數點後一位
print(m, '四捨五入到小數點後一位=', round(m,1))
print(n, '四捨五入到小數點後一位=', round(n,1))

# 比較運算
#
# 相等: ==
num_1 = 3
num_2 = 2
print(num_1, ' == ', num_2, '?')
print ('   ', num_1 == num_2)

# 不相等: !=
print(num_1, ' != ', num_2, '?')
print ('   ', num_1 != num_2)

# 大於: >
print(num_1, ' > ', num_2, '?')
print ('   ', num_1 > num_2)

# 小於: <
print(num_1, ' < ', num_2, '?')
print ('   ', num_1 < num_2)

# 大於或等於: >=
print(num_1, ' >= ', num_2, '?')
print ('   ', num_1 >= num_2)

# 小於或等於: <=
print(num_1, ' <= ', num_2, '?')
print ('   ', num_1 <= num_2)

# 類型轉換
#
num_1 = int('100')
print(num_1, '是整數，自己加自己...')
print('   ', num_1 + num_1, '是整數!')

str_1 = str(num_1)
print(str_1, '是字串，自己加自己...')
print('   ', str_1 + str_1, '是字串!')
